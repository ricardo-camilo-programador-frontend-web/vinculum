const Product = use('App/Models/Product')

class ProductController {
  async index({ request }) {
    const page = Number(request.input('page', 1))
    const perPage = 10
    return Product.query().paginate(page, perPage)
  }

  async store({ request }) {
    const productData = request.only(['name', 'description', 'price'])
    const newProduct = await Product.create(productData)
    return newProduct
  }

  async show({ params }) {
    const foundProduct = await Product.findOrFail(params.id)
    await foundProduct.load('categories')
    return foundProduct
  }

  async update({ params, request }) {
    const foundProduct = await Product.findOrFail(params.id)
    const updatedData = request.only(['name', 'description', 'price'])
    foundProduct.merge(updatedData)
    await foundProduct.save()
    return foundProduct
  }

  async destroy({ params }) {
    const foundProduct = await Product.findOrFail(params.id)
    await foundProduct.delete()
    return { message: 'Product deleted' }
  }
}

module.exports = ProductController
